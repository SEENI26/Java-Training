package orgoperator;

public class RelationalOperators {
	public static void main(String[] args) {
		
	int a = 10, b = 12;

    // Relational operators
	
    System.out.println("a is equal to b: " + (a == b));      // Checks if a is equal to b
    System.out.println("a is not equal to b: " + (a != b));  // Checks if a is not equal to b
    System.out.println("a is greater than b: " + (a > b));   // Checks if a is greater than b
    System.out.println("a is less than b: " + (a < b));      // Checks if a is less than b
    System.out.println("a is greater than or equal to b: " + (a >= b)); // Checks if a is greater than or equal to b
    System.out.println("a is less than or equal to b: " + (a <= b));    // Checks if a is less than or equal to b

}
}