package orgoperator;

public class TernaryOperator {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int max = (a > b) ? a : b; // Returns the greater of a and b
        System.out.println("\nTernary Operator:");
        System.out.println("Max of a and b: " + max);
	}

}
