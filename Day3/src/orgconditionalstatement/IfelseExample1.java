package orgconditionalstatement;

public class IfelseExample1 
{
public static void main(String[] args) {
		
		int num=11;
		// 11 divide by 2 equal to Zero if condition true 
		// 11 divide by 2 is Not equal to Zero if condition false so Execute Else 
		
		if(num%2==0)
		{
			System.out.println("Even number");
		}
		else
		{
			System.out.println("Odd number");
		}
		
		

	}

}
