package orgstring;

public class Stringlength 
{
	public static void main(String[] args) {
		 
		String n="Seenivasan";
		String p="9782824821";
		String j="j a v a p r o g r a m";
		// normal method
		int s=n.length();
		
		// smart method 
		System.out.println("string length is :" + n.length());
		System.out.println("Phone number length is :" + p.length());
		System.out.println("Space string is :" + j.length());
		System.out.println("string length is :" + s);
	}

}
