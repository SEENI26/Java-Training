package Loops;

// In for-loop easy to execute the statement
public class forloop 
{
	public static void main(String[] args) {
		
		// int i=1; is a installation
		// i<=10; is a condition
		// i++ is incrementation
		for(int i=1; i<=10; i++) 
		{
			System.out.println(i);
		}
	}

}
