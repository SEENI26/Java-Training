package Loops;

// In Another jumping statement is Continue is using to skip the Value then execute next value.
public class Jumpingstatement1 
{
	public static void main(String[] args) 
	{
		for(int i=1;i<=10;i++) {
			if(i==6)
			{
				continue;
			}
			System.out.println(i);
		}
		
		
	}

}
