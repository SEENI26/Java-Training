package Loops;

public class Whileloopexample4 
{
public static void main(String[] args) {
	
	int i=1;
	while(i<=10) 
	{
		// in if condition for odd number if(1%2==0) 
		// in if condition for even Number if(1%2!=0)
		if(i%2!=0) 
		{
			System.out.println(i);
		}
		i++;
	}
	
}
}
